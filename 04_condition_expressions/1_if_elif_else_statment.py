# ----- header
# if, elif, else statment adalah sebuah cara kita untuk membuat suatu milai/hasil pada kondisi tertentu.

# ----- start reading
# Perhatikan:
# * if statment adalah statment untuk mengeksekusi suatu perintah ketika kondisi-nya bernilai True.
# * elif(else if) statment adalah statment untuk mengeksekusi suatu perintah ketika kondisi dari statment if itu tidak menghasilkan True.
# * else statment adalah statment untuk pelengkap statment if.
# ----- end.

# ----- start coding
ujian_harian = 80
# if
if ( ujian_harian > 90 ):
    print("ujian kamu masuk kategory nilai A")
elif ( ujian_harian > 75 ):
    print("ujian kamu masuk kategory nilai B")
else:
    print("ujian kamu masuk kategory nilai C")
# ----- end.

