# concat adalah sebuah teknik dimana kita menggabungkan dua buah string atau lebih menggunakan (+)

# implementasi 
# kita memiliki variabel berikut
say = "Hello, my name is"
name = "Adam"
# lalu kita ingin menggabungkan variabel say dan variabel name
print( say + " " + name )
# output: Hello, my name is Adam